# Ek program banao jisme ek mytuple create karke count() aur index(), len() functions ka use karke output dikhayo.

mytuple = ('white', 'red', 'black', 'red')
print(mytuple)

# count() -- Counts how many times a value appears.
print(mytuple.count('red'))

# index -- Gives the position of the first matching value.
print(mytuple.index('red'))

# len
print(len(mytuple)) 
