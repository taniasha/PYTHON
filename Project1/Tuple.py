# Ek program banao jisme ek tuple create karke count() aur index(), len() functions ka use karke output dikhayo.

tuple = ('white', 'red', 'black', 'red')
print(tuple)

# count() -- Counts how many times a value appears.
print(tuple.count('red'))

# index -- Gives the position of the first matching value.
print(tuple.index('red'))

# len
print(len(tuple)) 
